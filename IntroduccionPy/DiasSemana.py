n=int(input("Numero de la semana:"))

if n==1:
    print("Es lunes")
elif n==2:
    print("Es martes")
elif n==3:
    print("Es miercoles")
elif n==4:
    print("Es jueves")
elif n==5:
    print("Es viernes")
elif n==6:
    print("Es sabado")
elif n==7:
    print("Es domingo")
else:
    print("No se corresponde a ningun dia de la semana")
